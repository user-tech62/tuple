# len()
my_tuple = (1, 2, 3)
print(len(my_tuple)) 

# max()
my_tuple = (1, 2, 3, 4)
print(max(my_tuple))

# min()
my_tuple = (1, 2, 3, 4)
print(min(my_tuple))  

# sum()
my_tuple = (1, 2, 3, 4)
print(sum(my_tuple)) 

# tuple()
my_list = [1, 2, 3]
my_tuple = tuple(my_list)
print(my_tuple)  

# index()
my_tuple = (1, 2, 3, 4)
print(my_tuple.index(3))  

# count()
my_tuple = (1, 2, 3, 2, 4, 2)
print(my_tuple.count(2))

